/**
 * Extract country from the "HQ / Founded" field.
 * E.g. "Czech Republic, founded 2015" -> "Czech Republic"
 *      "USA" -> "USA"
 */
function extractCountry(hqFounded) {
  if (!hqFounded) return null;
  // Remove "founded YYYY" or "est. YYYY" suffixes
  let country = hqFounded
    .replace(/,?\s*founded\s+\d{4}/i, '')
    .replace(/,?\s*est\.\s*\d{4}/i, '')
    .replace(/,?\s*broker\s+est\.\s*\d{4}/i, '')
    .replace(/,?\s*established\s+\d{4}/i, '')
    .replace(/,?\s*established\s+firm/i, '')
    .replace(/,?\s*newer/i, '')
    .replace(/,?\s*regulated broker arm/i, '')
    .trim();
  // Remove trailing commas
  country = country.replace(/,\s*$/, '').trim();
  return country || hqFounded;
}

/**
 * Generate a URL-friendly slug from the firm name.
 */
function generateSlug(name) {
  return name
    .toLowerCase()
    .replace(/[()]/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '');
}

async function main() {
  console.log(`Seeding ${firms.length} prop firms from spreadsheet data...`);

  let created = 0;
  let updated = 0;

  for (const firm of firms) {
    const slug = generateSlug(firm.name);
    const country = extractCountry(firm.hq_founded);

    const data = {
      name: firm.name,
      slug,
      country,
      founded_year: firm.founded_year || null,
      assets: firm.markets_offered || [],
      platforms: firm.platforms || [],
      max_allocation: firm.max_allocation || null,
      description: firm.key_features || null,
      category: firm.category || null,
      markets_offered: firm.markets_offered || [],
      account_sizes: firm.account_sizes || null,
      evaluation_model: firm.evaluation_model || null,
      profit_split: firm.profit_split || null,
      drawdown_type: firm.drawdown_type || null,
      daily_loss_limit: firm.daily_loss_limit || null,
      consistency_rule: firm.consistency_rule || null,
      min_trading_days: firm.min_trading_days || null,
      news_trading: firm.news_trading || null,
      payout_frequency: firm.payout_frequency || null,
      key_features: firm.key_features || null,
      reputation_notes: firm.reputation_notes || null,
      is_listed: true,
    };

    try {
      const existing = await prisma.firm.findUnique({ where: { slug } });
      if (existing) {
        await prisma.firm.update({ where: { slug }, data });
        updated++;
      } else {
        await prisma.firm.create({ data });
        created++;
      }
    } catch (error) {
      console.error(`Error upserting firm "${firm.name}" (slug: ${slug}):`, error.message);
    }
  }

  console.log(`\nSeeding complete! Created: ${created}, Updated: ${updated}, Total: ${firms.length}`);
}

main()
  .catch((e) => {
    console.error('Seeding failed:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
